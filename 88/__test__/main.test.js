import { expect, test } from "vitest"
import { merge } from "../src/main.js"


test("example 1", () => {
    //arrange
    const nums1 = [1,2,3,0,0,0];
    const m = 3;
    const nums2 = [2,5,6];
    const n = 3;

    //act
    merge(nums1, m, nums2, n);

    //assert
    expect(nums1).toEqual([1,2,2,3,5,6])
})

